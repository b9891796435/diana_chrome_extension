class App extends React.Component<{}, { theme: members }> {
  constructor(props: any) {
    super(props);
    this.state = {
      theme: "carol"
    }
  }
  async componentDidMount() {
    this.setState({
      theme: await chromeGet("theme")
    })
  }
  async openToolKit() {
    window.open(chrome.runtime.getURL("newTab.html") + "#toolKit")
    window.close()
  }
  async openOptionPage() {
    chrome.runtime.openOptionsPage();
    window.close()
  }
  render(): React.ReactNode {
    return (
      <div className={"App " + this.state.theme}>
        <div>
          <img src={memberList[this.state.theme].post} alt="" />
        </div>
        <div onClick={this.openToolKit}>
          {ToolIcon}
          <span>工具箱</span>
        </div>
        <div onClick={this.openOptionPage}>
          {SettingIcon}
          <span>设置</span>
        </div>
      </div>
    );
  }
}

export default App
